const languageButton = document.getElementById("languageButton");
const languageMenu = document.getElementById("languageMenu");
const currentLanguageLabel = document.getElementById("currentLanguageLabel");
const languageOptions = document.querySelectorAll(".language-option");

function closeLanguageMenu() {
  if (!languageMenu || !languageButton) return;
  languageMenu.classList.remove("open");
  languageButton.setAttribute("aria-expanded", "false");
}

function setLanguage(language) {
  const lang = translations[language] ? language : "ms";
  const dictionary = translations[lang];

  document.documentElement.lang = lang;
  document.title = dictionary.pageTitle;

  const metaDescription = document.getElementById("meta-description");
  if (metaDescription) metaDescription.content = dictionary.metaDescription;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key] !== undefined) element.textContent = dictionary[key];
  });

  if (currentLanguageLabel) currentLanguageLabel.textContent = lang === "ms" ? "BM" : "EN";

  languageOptions.forEach((option) => {
    const active = option.dataset.language === lang;
    option.classList.toggle("active", active);
    option.setAttribute("aria-current", active ? "true" : "false");
  });

  localStorage.setItem("suoLanguage", lang);
  closeLanguageMenu();
}

languageButton?.addEventListener("click", (event) => {
  event.stopPropagation();
  const isOpen = languageMenu.classList.toggle("open");
  languageButton.setAttribute("aria-expanded", String(isOpen));
});

languageOptions.forEach((option) => {
  option.addEventListener("click", () => setLanguage(option.dataset.language));
});

document.addEventListener("click", (event) => {
  if (!event.target.closest(".language-switcher")) closeLanguageMenu();
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") closeLanguageMenu();
});

setLanguage(localStorage.getItem("suoLanguage") || "ms");

document.querySelectorAll('a[href^="#"]').forEach((link) => {
  link.addEventListener("click", (event) => {
    const targetId = link.getAttribute("href");
    if (!targetId || targetId === "#") return;

    const target = document.querySelector(targetId);
    if (!target) return;

    event.preventDefault();
    target.scrollIntoView({ behavior: "smooth", block: "start" });
  });
});

const navLinks = document.querySelectorAll(".nav-links a[href^='#']");
const sections = document.querySelectorAll("main section[id]");

const navObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) return;

    navLinks.forEach((link) => {
      link.classList.toggle(
        "active",
        link.getAttribute("href") === `#${entry.target.id}`
      );
    });
  });
}, { rootMargin: "-35% 0px -55% 0px", threshold: 0 });

sections.forEach((section) => navObserver.observe(section));

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

function animateCounter(counter) {
  const target = Number(counter.dataset.target || 0);
  const duration = 1200;
  const start = performance.now();

  function update(now) {
    const progress = Math.min((now - start) / duration, 1);
    counter.textContent = Math.floor(target * progress);

    if (progress < 1) {
      requestAnimationFrame(update);
    } else {
      counter.textContent = target;
    }
  }

  requestAnimationFrame(update);
}

const counterObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      animateCounter(entry.target);
      counterObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.6 });

document.querySelectorAll(".counter").forEach((counter) => counterObserver.observe(counter));


